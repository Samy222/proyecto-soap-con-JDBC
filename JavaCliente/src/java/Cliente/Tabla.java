    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        try{
            if(!txtNombre.getText().isEmpty() && !txtPrecio.getText().isEmpty() && !txtStock.getText().isEmpty() && !txtProveedor.getText().isEmpty()){
                String name = txtNombre.getText();
                float price = Float.parseFloat(txtPrecio.getText());
                int stock = Integer.parseInt(txtStock.getText());
                String supplier = txtProveedor.getText();
                
                
                
                
                JOptionPane.showMessageDialog(null, registrar(name,price,stock,supplier), "Mensaje de confirmaci√≥n", JOptionPane.INFORMATION_MESSAGE);
                
                txtNombre.setText(null);
                txtPrecio.setText(null);
                txtStock.setText(null);
                txtProveedor.setText(null);
                btnEditar.setVisible(false);
                btnEliminar.setVisible(false);
                cargarDatos();
            }else{
                JOptionPane.showMessageDialog(null, "Campos vac√≠os, agregue datos", "Error!!", JOptionPane.ERROR_MESSAGE);
            }
            
        }catch(Exception ex){
            System.out.println(ex);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void jtProductosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtProductosMouseClicked
         int fila = jtProductos.rowAtPoint(evt.getPoint());
         int columna = jtProductos.columnAtPoint(evt.getPoint());
         int col = 0;
         if ((fila > -1) && (columna > -1))
             idSelec = (int) modelo.getValueAt(fila,0);
            nombreEliminar = modelo.getValueAt(fila,1).toString();
         
         txtNombre.setText(modelo.getValueAt(fila,1).toString());
        txtPrecio.setText(modelo.getValueAt(fila,2).toString());
        txtStock.setText(modelo.getValueAt(fila,3).toString());
        txtProveedor.setText(modelo.getValueAt(fila,4).toString());
        btnEliminar.setVisible(true);
        btnEditar.setVisible(true);
    }//GEN-LAST:event_jtProductosMouseClicked

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        Servicios_Service service = new Servicios_Service();
        Servicios port = service.getServiciosPort();
        if (JOptionPane.showConfirmDialog(null, "¬øEst√° seguro que desea elimiar el producto " + nombreEliminar + "?", "Eliminar Producto", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == JOptionPane.YES_OPTION){
            JOptionPane.showMessageDialog(null, port.delete(idSelec));
            cargarDatos();
            btnEditar.setVisible(false);
            btnEliminar.setVisible(false);
            txtNombre.setText(null);
            txtPrecio.setText(null);
            txtStock.setText(null);
            txtProveedor.setText(null);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if(!txtNombre.getText().isEmpty() && !txtPrecio.getText().isEmpty() && !txtStock.getText().isEmpty() && !txtProveedor.getText().isEmpty()){
                String name = txtNombre.getText();
                float price = Float.parseFloat(txtPrecio.getText());
                int stock = Integer.parseInt(txtStock.getText());
                String supplier = txtProveedor.getText();
                Servicios_Service service = new Servicios_Service();
                Servicios port = service.getServiciosPort();
                JOptionPane.showMessageDialog(null, port.update(idSelec, name, price, stock, supplier));
                txtNombre.setText(null);
                txtPrecio.setText(null);
                txtStock.setText(null);
                txtProveedor.setText(null);
                btnEditar.setVisible(false);
                btnEliminar.setVisible(false);
                cargarDatos();
            }else{
                JOptionPane.showMessageDialog(null, "Campos vac√≠os, complete todos los campos!", "Existen campos vac√≠os", JOptionPane.ERROR_MESSAGE);
            }
    }//GEN-LAST:event_btnEditarActionPerformed

    
